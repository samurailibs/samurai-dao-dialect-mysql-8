DROP TABLE BINARY_TABLE;
DROP TABLE DEPT;
DROP TABLE EMP;
DROP TABLE TWO_PK_TABLE;
DROP VIEW  EMP_VIEW;

CREATE TABLE BINARY_TABLE (
      ID  INTEGER NOT NULL PRIMARY KEY
    , BIN VARBINARY(2048))ENGINE=INNODB;

CREATE TABLE DEPT
       (DEPTNO INTEGER NOT NULL PRIMARY KEY,
        DNAME TEXT,
        LOC TEXT,
        VERSIONNO INTEGER)ENGINE=INNODB;

CREATE TABLE EMP
       (EMPNO INTEGER NOT NULL PRIMARY KEY AUTO_INCREMENT,
        ENAME TEXT,
        JOB TEXT,
        MGR INTEGER,
        HIREDATE TIMESTAMP(4),
        SAL REAL,
        COMM REAL,
        DEPTNO INTEGER,
        TSTAMP TIMESTAMP(4),
        FOREIGN KEY(DEPTNO) REFERENCES DEPT(DEPTNO)
        )ENGINE=INNODB;

